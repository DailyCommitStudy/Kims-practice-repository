-- 2022 8 ~ 10 총 대여횟수가 5회 이상인 것
-- 월별 자동차 ID별 총 대여 횟수 (ㄱRECORDS)
-- 정렬도
-- 특정 월의 총 대여 횟수가 0인 경우는 ㅈㅔ외하라고..?

SELECT COUNT(*) AS RECORDS
FROM CAR_RENTAL_COMPANY_RENTAL_HISTORY
GROUP BY CAR_ID
WHERE YEAR(START_DATE) = "2022" AND "8" <= MONTH(START_DATE) AND MONTH(START_DATE) <= "10"
HAVING COUNT(CAR_ID) >= 5
ORDER BY MONTH(START_DATE) ASC, CAR_ID ASC